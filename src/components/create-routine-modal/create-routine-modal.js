import './create-routine-modal.css';
import { saveRoutine } from '../../js/modules/routineModule.js';
import { saveSession } from '../../js/modules/sessionModule.js';
import { DAY_NAMES } from '../../js/entities/session.js';
import { DetailsDialog } from '../details-dialog/details-dialog.js';
import { qs } from '../../js/utils/dom.js';

let routineDialog = null;
let onCreatedCallback = null;

function renderFormHTML() {
  const dayCheckboxes = DAY_NAMES.map(
    (name, i) =>
      `<label class="checkbox-label">
        <input type="checkbox" name="day" value="${i}" ${i < 5 ? 'checked' : ''} /> ${name}
      </label>`
  ).join('');

  return `
    <div class="form-group">
      <label for="routine-name">Routine Name</label>
      <input id="routine-name" type="text" placeholder="e.g., Push/Pull/Legs" />
    </div>
    <div class="form-group">
      <label for="routine-description">Description (optional)</label>
      <textarea id="routine-description" placeholder="e.g., 6-day split focusing on compound movements"></textarea>
    </div>
    <div class="form-group">
      <label>Auto-create Sessions</label>
      <div class="day-selector">${dayCheckboxes}</div>
    </div>
    <div class="modal-actions">
      <button type="button" id="btn-cancel" class="secondary">Cancel</button>
      <button type="button" id="btn-create">Create Routine</button>
    </div>
  `;
}

function bindFormEvents(dialog) {
  qs('#btn-cancel').addEventListener('click', () => dialog.close());
  qs('#btn-create').addEventListener('click', () => submitCreate(dialog));
}

function submitCreate(dialog) {
  const name = qs('#routine-name').value.trim();
  if (!name) {
    alert('Please enter a routine name');
    return;
  }

  const description = qs('#routine-description').value.trim();
  const selectedDays = Array.from(
    document.querySelectorAll('input[name="day"]:checked')
  ).map((cb) => parseInt(cb.value));

  const routine = saveRoutine({ name, description, status: 'active' });

  selectedDays.forEach((dayOfWeek, index) => {
    saveSession({
      routineId: routine.id,
      name: DAY_NAMES[dayOfWeek],
      dayOfWeek,
      order: index,
      isAutoGenerated: true,
    });
  });

  dialog.close();
  if (onCreatedCallback) onCreatedCallback(routine);
}

export function openCreateModal(onCreated) {
  onCreatedCallback = onCreated;
  if (!routineDialog) {
    routineDialog = new DetailsDialog();
  }
  routineDialog.setTitle('Create New Routine');
  routineDialog.setContent(renderFormHTML());
  bindFormEvents(routineDialog);
  routineDialog.open();
}
